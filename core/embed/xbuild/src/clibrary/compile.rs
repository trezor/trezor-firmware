use std::path::{Path, PathBuf};

use color_eyre::{Result, eyre::WrapErr};

use super::CLibrary;
use crate::attrs::CompileAttrs;
use crate::dep_tracking::{run_command, run_command_with_cc_dep};
use crate::parallel::run_parallel;

use crate::helpers::{
    derive_output_path, ensure_parent_directory, join_paths_lexically, links_name, measure_time,
    path_from_env,
};

// Represents a single compilation task
#[derive(Clone)]
struct CompileUnit {
    // Index of the compile unit, used for ordering artifacts
    // after parallel compilation
    index: usize,
    // Path to the input source file to be compiled
    input: PathBuf,
    // Path to the output object file generated by the compiler
    output: PathBuf,
    // Path to the dependency file generated by the compiler (e.g., .d file)
    cc_dep: Option<PathBuf>,
    // Optional per-source compile attributes that are merged with the
    // library-level attributes
    attrs: Option<CompileAttrs>,
}

// Represents the result of compiling a single source file
struct CompileArtifact {
    // Index of the compile unit, used to maintain the original order of units
    index: usize,
    // Path to the output object file generated by the compiler
    output: PathBuf,
}

impl CompileUnit {
    fn compile(&self, tool: &cc::Tool) -> Result<CompileArtifact> {
        let mut cmd = tool.to_command();

        if let Some(attrs) = &self.attrs {
            for arg in attrs.to_compiler_args() {
                cmd.arg(arg);
            }
        }

        if let Some(cc_dep) = &self.cc_dep {
            // Instruct the compiler to generate a dependency file for this source.
            cmd.arg("-MMD").arg("-MF").arg(cc_dep);
        }

        cmd.arg("-c").arg("-o").arg(&self.output).arg(&self.input);

        run_command_with_cc_dep(
            &mut cmd,
            [&self.input],
            [&self.output],
            self.cc_dep.as_deref(),
        )
        .with_context(|| format!("Failed to compile {}", self.input.display()))?;

        Ok(CompileArtifact {
            index: self.index,
            output: self.output.clone(),
        })
    }
}

impl CLibrary {
    /// Compiles all sources in parallel and generates object files for the library.
    ///
    /// This function compiles all source files associated with the library in parallel,
    /// creates a static library archive, and exports the library's metadata for downstream crates.
    ///
    /// # Errors
    ///
    /// Returns an error if compilation or archiving fails, or if required environment variables are missing.
    pub(crate) fn compile(&self) -> Result<()> {
        let lib_name = links_name()?;

        measure_time(format!("@@ {} compiled in", lib_name), || {
            // Run parallel build on all sources
            let objects = self.process_sources("o", None, None)?;
            // Append manually added objects (e.g., vendor header)
            let objects = objects
                .into_iter()
                .chain(self.get_objects().into_iter().cloned())
                .collect::<Vec<_>>();
            // Build static library from all objects
            make_static_library(objects, &lib_name)?;
            // Export library metadata for downstream crates
            self.export_library_metadata()?;
            Ok::<(), color_eyre::eyre::Report>(())
        })
        .context(format!("Failed to build `{}` library", lib_name))
    }

    /// Runs the compiler/preprocessor on all sources in parallel, producing
    /// output files with the given extension.
    ///
    /// # Parameters
    ///
    /// - `output_ext`: The file extension for the output files (e.g., `"upydef"`).
    /// - `extra_args`: Additional compiler/preprocessor flags.
    /// - `extra_sources`: Additional source files to process beyond the library's own sources.
    ///
    /// # Returns
    ///
    /// A vector of paths to the generated output files.
    pub fn process_sources(
        &self,
        output_ext: &str,
        extra_args: Option<&[&str]>,
        extra_sources: Option<&[PathBuf]>,
    ) -> Result<Vec<PathBuf>> {
        let out_dir = path_from_env("OUT_DIR")?;
        let base_dir = path_from_env("CARGO_MANIFEST_DIR")?;

        // Prepare list of all sources, including extra sources if provided
        let mut sources: Vec<(PathBuf, Option<CompileAttrs>)> = self
            .get_sources()
            .into_iter()
            .map(|entry| (entry.path.clone(), entry.attrs.clone()))
            .collect();
        if let Some(extra) = extra_sources {
            sources.extend(extra.iter().cloned().map(|src| (src, None)));
        }

        // Prepare compile units for all source files
        let mut units = Vec::with_capacity(sources.len());
        for (index, (src, attrs)) in sources.into_iter().enumerate() {
            // Derive absolute paths for input and output files
            let input = join_paths_lexically(&base_dir, &src);
            let output = derive_output_path(&base_dir, &src, &out_dir, output_ext);

            // Only generate .d files for object files compiled from C/C++ sources
            let cc_dep = if output_ext == "o"
                && src
                    .extension()
                    .is_some_and(|ext| ext == "c" || ext == "cpp" || ext == "cc")
            {
                Some(output.with_extension("d"))
            } else {
                None
            };

            ensure_parent_directory(&output)?;

            units.push(CompileUnit {
                index,
                input,
                output,
                cc_dep,
                attrs,
            });
        }

        // Merge public and private attributes
        let mut attrs = self.get_merged_attrs();

        // Add extra flags if needed
        if let Some(args) = extra_args {
            for arg in args {
                attrs.add_flag(arg);
            }
        }

        // Compile all units in parallel
        let artifacts = compile_parallel(units, &attrs)?;
        let outputs = artifacts.into_iter().map(|a| a.output).collect();

        Ok(outputs)
    }

    /// Preprocesses a C header or source file using the configured C compiler.
    ///
    /// This runs the compiler in preprocessor mode (`-E`) and writes the output to the specified file.
    ///
    /// # Parameters
    ///
    /// - `input`: Path to the input C header or source file.
    /// - `output`: Path to the output file where the preprocessed result will be written.
    ///
    /// # Errors
    ///
    /// Returns an error if preprocessing fails.
    pub fn preprocess_file(&self, input: &Path, output: &Path) -> Result<()> {
        let mut cmd = self
            .get_merged_attrs()
            .get_configured_compiler()
            .to_command();

        cmd.arg("-E").arg(input).arg("-o").arg(output);

        run_command(&mut cmd, [input], [output])
            .with_context(|| format!("Failed to preprocess {}", input.display(),))
    }

    /// Exports the library's public compile attributes as Cargo metadata
    /// for downstream crates. They can be imported by CLibrary::import_crate.
    fn export_library_metadata(&self) -> Result<()> {
        // Public attributes
        self.get_public_attrs().export_as_metadata()?;

        // Export linked libraries (so the top-level crate can re-export them)
        println!(
            "cargo::metadata=public_c_libs={}",
            self.get_libs()
                .into_iter()
                .map(|lib| lib.to_string())
                .collect::<Vec<_>>()
                .join(";")
        );
        Ok(())
    }
}

// Compiles multiple source files sequentially in the current thread.
#[allow(dead_code)]
fn compile_sequential(
    units: Vec<CompileUnit>,
    attrs: &CompileAttrs,
) -> Result<Vec<CompileArtifact>> {
    let tool = attrs.get_configured_compiler();
    let mut compiled = Vec::with_capacity(units.len());

    for unit in units {
        compiled.push(unit.compile(&tool)?);
    }

    Ok(compiled)
}

// Compiles multiple source files in parallel using worker threads.
fn compile_parallel(units: Vec<CompileUnit>, attrs: &CompileAttrs) -> Result<Vec<CompileArtifact>> {
    let tool = attrs.get_configured_compiler();

    let compile_func = move |unit: &CompileUnit| unit.compile(&tool);

    let mut artifacts = run_parallel(units, compile_func)?;

    artifacts.sort_by_key(|artifact| artifact.index);

    Ok(artifacts)
}

// Batch all objects into one static library
fn make_static_library(objects: Vec<PathBuf>, lib_name: &str) -> Result<()> {
    let out_dir = path_from_env("OUT_DIR")?;

    let output = out_dir.join(format!("lib{}.a", lib_name));

    let mut cmd = cc::Build::new().get_archiver();
    cmd.arg("rcs").arg(&output).args(&objects);

    run_command(&mut cmd, &objects, [&output])
        .with_context(|| format!("Failed to create static library {}", output.display()))?;

    // Expose archive directory to the linker, both for rebuilt and cached archives
    println!("cargo:rustc-link-search=native={}", out_dir.display());

    Ok(())
}
