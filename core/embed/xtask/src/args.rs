pub struct ResolvedBuild {
    pub features: Vec<String>,
    pub target_triple: Option<&'static str>,
}

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum Component {
    Bootloader,
    Boardloader,
    #[value(name = "bootloader_ci")]
    BootloaderCi,
    Firmware,
    Prodtest,
    Kernel,
    Secmon,
}

impl Component {
    /// The Rust package name used for cargo commands
    pub fn package_name(self, emulator: bool) -> &'static str {
        match self {
            Component::Bootloader => "bootloader",
            Component::Boardloader => "boardloader",
            Component::BootloaderCi => "bootloader_ci",
            Component::Firmware => {
                if emulator {
                    "unix"
                } else {
                    "firmware"
                }
            }
            Component::Prodtest => "prodtest",
            Component::Kernel => "kernel",
            Component::Secmon => "secmon",
        }
    }

    /// The linker binary type used by `build_and_link()`. This determines
    /// the `.map` filename generated by the linker.
    pub fn binary_name(self) -> &'static str {
        match self {
            Component::Bootloader => "bootloader",
            Component::BootloaderCi => "bootloader_ci",
            Component::Boardloader => "boardloader",
            Component::Firmware => "firmware",
            Component::Prodtest => "prodtest",
            Component::Kernel => "kernel",
            Component::Secmon => "secmon",
        }
    }

    /// The artifact name used for collected build outputs.
    /// Appends `-emu` for emulator builds.
    pub fn artifact_name(self, emulator: bool) -> String {
        let base = self.binary_name();
        if emulator {
            format!("{base}-emu")
        } else {
            base.to_string()
        }
    }

    /// Get symbol from memory.ld used as start address for flashing
    pub fn flash_start_symbol(self) -> Result<&'static str> {
        let symbol = match self {
            Component::Boardloader => "BOARDLOADER_START",
            Component::Bootloader | Component::BootloaderCi => "BOOTLOADER_START",
            Component::Firmware | Component::Prodtest => "FIRMWARE_START",
            _ => return Err(anyhow!("Flashing {:?} is not supported", self)),
        };
        Ok(symbol)
    }

    /// Returns the next component in the dependency chain,
    /// (e.g. Firmware -> Kernel -> Secmon)
    pub fn dependency(self, model: Model) -> Option<Component> {
        match self {
            Component::Firmware => Some(Component::Kernel),
            Component::Kernel => {
                let has_secmon = config::ModelConfig::load(model.model_id())
                    .map(|c| c.secmon)
                    .unwrap_or(false);
                if has_secmon {
                    Some(Component::Secmon)
                } else {
                    None
                }
            }
            _ => None,
        }
    }

    /// Returns whether the component can be flashed to a device
    /// using the `flash` subcommand.
    pub fn flashable(self) -> bool {
        self.flash_start_symbol().is_ok()
    }

    /// Returns whether the component can be uploaded to a device
    /// using the `upload` subcommand.
    pub fn uploadable(self) -> bool {
        matches!(self, Component::Firmware | Component::Prodtest)
    }
}

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum ConsoleType {
    Vcp,
    Swo,
    SystemView,
}

#[derive(Parser, Debug)]
#[command(name = "xtask")]
#[command(about = "Trezor workspace automation tasks")]
pub struct Cli {
    #[command(subcommand)]
    pub command: Cmd,
}

#[derive(Subcommand, Debug)]
pub enum Cmd {
    /// Build a component with the specified configuration
    Build(BuildArgs),
    /// Run clippy command with the specified configuration
    Clippy(BuildArgs),
    /// Run check command with the specified configuration
    Check(BuildArgs),
    /// Run tests of specified package
    Test(TestArgs),
    /// Clean build artifacts
    Clean,
    /// Format code with rustfmt
    Fmt,
    /// Flash firmware to device using OpenOCD
    Flash(FlashArgs),
    /// Erase flash or part of it using OpenOCD
    FlashErase(FlashEraseArgs),
    /// Upload firmware to device
    Upload(UploadArgs),
    /// Combine multiple firmware components into a single binary for flashing
    Combine(CombineArgs),
}

#[derive(Args, Debug, Clone)]
#[command(override_usage = "xtask build <COMPONENT> --model <MODEL> [OPTIONS]")]
pub struct BuildArgs {
    pub component: Component,

    /// Build target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,

    /// Use emulator build
    #[arg(long, short = 'e')]
    pub emulator: bool,

    /// Enable debug build
    #[arg(long, short = 'd', num_args = 0..=1, default_missing_value = "true")]
    pub debug: Option<bool>,

    /// Debug console backend
    #[arg(long)]
    pub dbg_console: Option<ConsoleType>,

    /// Build Bitcoin-only firmware
    #[arg(long)]
    pub btc_only: bool,

    /// Enable production build
    #[arg(long)]
    pub production: bool,

    /// Force bootloader upgrade
    #[arg(long)]
    pub force_bootloader_upgrade: bool,

    /// Use dev bootloader
    #[arg(long)]
    pub bootloader_devel: bool,

    /// Enable unsafe firmware features
    #[arg(long)]
    pub unsafe_fw: bool,

    /// Embed frozen MicroPython modules
    #[arg(long)]
    pub frozen: bool,

    /// Include MicroPython source lines
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub source_lines: Option<bool>,

    /// Optimize MicroPython bytecode
    #[arg(long, num_args = 0..=1, default_missing_value = "true", overrides_with = "pyopt")]
    pub pyopt: Option<bool>,

    /// Enable Micropython memory performance measurements
    #[arg(long)]
    pub mem_perf: bool,

    /// Enable debug link
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub debug_link: Option<bool>,

    /// Enable N4W1 support
    #[arg(long)]
    pub n4w1: bool,

    /// Disable UI animations
    #[arg(long)]
    pub disable_animation: bool,

    /// Show UI perf overlay
    #[arg(long)]
    pub perf_overlay: bool,

    /// Include crypto benchmarks
    #[arg(long)]
    pub benchmark: bool,

    /// Log stack usage
    #[arg(long)]
    pub log_stack_usage: bool,

    /// Use blocking VCP writes, in order to allow reliable debug data transmission over VCP.
    /// Disabled by default, to prevent debug firmware from getting stuck while writing log messages (if the host is not reading them).
    #[arg(long)]
    pub block_on_vcp: bool,

    /// Enable Address Sanitizer (ASAN) instrumentation
    #[arg(long)]
    pub asan: bool,

    /// Enable external app loading
    #[arg(long)]
    pub apps: bool,

    /// Disable OPTIGA support
    #[arg(long)]
    pub disable_optiga: bool,

    /// Board revision to build for (defaults to model's default_board)
    #[arg(long, short = 'b')]
    pub board: Option<String>,

    /// Disable TROPIC support
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub disable_tropic: Option<bool>,

    /// Enable insecure storage test mode
    #[arg(long)]
    pub storage_insecure_testing_mode: bool,

    /// Emits memory analysis output (type sizes and stack sizes)
    #[arg(long)]
    pub emit_memory_analysis: bool,

    /// Output cargo timings
    #[arg(long)]
    pub timings: bool,

    /// Enable verbose output
    #[arg(long)]
    pub verbose: bool,
}

impl BuildArgs {
    /// Determines the Cargo profile to use
    pub fn profile_name(&self) -> &'static str {
        if self.emulator && self.debug.unwrap_or(true) {
            "dev"
        } else if !self.emulator && self.debug.unwrap_or(false) {
            "debug-opt"
        } else {
            "release"
        }
    }

    /// Resolves cargo features and target triple from the provided cli arguments.
    pub fn resolve_features(&self) -> Result<ResolvedBuild> {
        let mut features: Vec<String> = vec![self.model.feature_name()];

        if self.emulator {
            features.push("emulator".into());
        }

        if self.production {
            features.push("production".into());
        }

        if self.bootloader_devel {
            features.push("bootloader_devel".into());
        }

        // if self.production && self.bootloader_devel {
        //    bail!("bootloader-devel cannot be enabled in production builds");
        // }

        if self.force_bootloader_upgrade {
            features.push("force_bootloader_upgrade".into());
        }

        if self.emulator {
            features.push("dbg_console".into());

            if self.asan {
                features.push("asan".into());
            }
        } else {
            match (self.component, self.dbg_console) {
                (Component::Firmware, Some(_)) => features.push("dbg_console".into()),
                (Component::Secmon, Some(ConsoleType::Vcp)) => (),
                (Component::Boardloader, Some(ConsoleType::Vcp)) => (),
                (Component::Prodtest, Some(ConsoleType::Vcp)) => (),
                (_, Some(ConsoleType::Vcp)) => features.push("dbg_console_vcp".into()),
                (_, Some(ConsoleType::Swo)) => features.push("dbg_console_swo".into()),
                (_, Some(ConsoleType::SystemView)) => features.push("dbg_console_sysview".into()),
                (_, None) => (),
            }
        }

        let pyopt = self.pyopt.unwrap_or(true);

        if self.component == Component::Firmware {
            if pyopt {
                features.push("pyopt".into());
            } else {
                features.push("debug".into());
            }

            if self.frozen || !self.emulator {
                features.push("frozen");
            }

            if self.source_lines.unwrap_or(self.emulator) {
                features.push("micropy_enable_source_lines".into());
            }

            if self.benchmark {
                features.push("benchmark".into());
            }

            if self.log_stack_usage {
                features.push("log_stack_usage".into());
            }

            if self.block_on_vcp {
                features.push("block_on_vcp".into());
            }

            if self.apps {
                features.push("app_loading".into());
            }

            if self.mem_perf {
                features.push("memperf".into());
            }

            if !self.production {
                features.push("dev_keys".into());
            }

            if self.n4w1 {
                features.push("n4w1".into());
            }
        }

        if matches!(
            self.component,
            Component::Secmon | Component::Kernel | Component::Firmware
        ) {
            if !self.btc_only {
                features.push("universal_fw".into());
            }

            if !pyopt {
                features.push("optiga_testing".into());
            }

            if self.unsafe_fw {
                features.push("unsafe_fw".into());
            }

            if self.storage_insecure_testing_mode {
                if self.production {
                    bail!("storage_insecure_testing_mode cannot be enabled in production builds");
                }
                features.push("storage_insecure_testing_mode".into());
            }
        }

        if matches!(
            self.component,
            Component::Firmware | Component::Bootloader | Component::Prodtest
        ) {
            if self.perf_overlay {
                features.push("ui_performance_overlay".into());
            }

            if self.debug_link.unwrap_or(!pyopt) {
                features.push("debuglink".into());
                features.push("ui_debug".into());
            }

            if self.disable_animation {
                features.push("disable_animation".into());
            }
        }

        if matches!(self.component, Component::Kernel) && self.debug_link.unwrap_or(!pyopt) {
            features.push("debuglink".into());
        }

        if matches!(
            self.component,
            Component::Firmware | Component::Kernel | Component::Secmon | Component::Prodtest
        ) {
            if self.model.has_optiga() && !self.disable_optiga {
                features.push("optiga".into());
            }

            if self.model.has_tropic() && !self.disable_tropic.unwrap_or(self.emulator) {
                features.push("tropic".into());
            }
        }

        // By default, we want to build with `frozen` for hardware targets
        if self
            .frozen
            .unwrap_or(self.component.frozen_default(self.emulator))
        {
            features.push("frozen".into());
        }

        // Board and model-intrinsic features from TOML config
        let model_config = config::ModelConfig::load(self.model.model_id())?;
        let board_id = if self.emulator {
            model_config
                .emulator_board
                .as_deref()
                .ok_or_else(|| anyhow!("Model {} has no emulator board", self.model.model_id()))?
                .to_string()
        } else {
            self.board
                .clone()
                .unwrap_or_else(|| model_config.default_board.clone())
        };
        let mut board_feat = config::resolve_board_features(
            self.model.model_id(),
            &model_config,
            &board_id,
            self.component,
        )?
        .features;
        if self.disable_optiga {
            board_feat.retain(|f| f != "optiga");
        }
        if self.disable_tropic.unwrap_or(self.emulator) {
            board_feat.retain(|f| f != "tropic");
        }
        features.extend(board_feat);

        let target_triple = if self.emulator {
            None
        } else {
            Some(model_config.target_triple()?)
        };

        Ok(ResolvedBuild {
            features,
            target_triple,
        })
    }

    // Configures the cargo command with the appropriate arguments and features
    // based on the provided cli arguments
    pub fn configure_cargo(&self, cmd: &mut process::Command) -> Result<()> {
        let resolved = self.resolve_features()?;
        let mut rebuild_std = false;

        cmd.args(["--package", self.component.package_name(self.emulator)]);
        cmd.args(["--features", &resolved.features.join(",")]);
        cmd.args(["--profile", self.profile_name()]);

        if self.profile_name() == "release" {
            // Required by panic-immediate-abort in the release profile
            rebuild_std = true;
        }

        if let Some(triple) = resolved.target_triple {
            cmd.args(["--target", triple]);
        }

        if self.emit_memory_analysis {
            // See https://nnethercote.github.io/perf-book/type-sizes.html#measuring-type-sizes for more details
            //
            // Use --config instead of RUSTFLAGS env so that rustflags in .cargo/config.toml are
            // not overridden (RUSTFLAGS env has higher precedence and replaces them entirely).
            cmd.args(["--config", "build.rustflags=[\"-Zprint-type-sizes\"]"]);
        }

        if self.emulator && self.asan {
            // -Zsanitizer=address is a rustc flag passed via RUSTFLAGS.
            //
            // Without an explicit --target, cargo compiles proc-macros and the firmware in the
            // same pass and RUSTFLAGS leaks into proc-macro crates, causing "can't find crate"
            // errors. Passing --target explicitly (even the same triple as the host) makes cargo
            // separate the host (proc-macros / build scripts) and target (firmware) compilation
            // units, so RUSTFLAGS only reaches the firmware crates.
            //
            cmd.args(["--target", &helpers::host_triple()?]);
            cmd.args([
                "--config",
                "build.rustflags=[\"-Zsanitizer=address\", \"-Clink-arg=-lgcc_s\"]",
            ]);

            // Rebuild  standard library to be compiled with sanitizer instrumentation
            rebuild_std = true;
        }

        if self.timings {
            cmd.arg("--timings");
        }

        if self.verbose {
            cmd.arg("--verbose");
        }

        if rebuild_std {
            cmd.arg("-Zbuild-std=core");
        }

        Ok(())
    }
}

