use anyhow::{Result, anyhow};
use clap::{Args, Parser, Subcommand, ValueEnum};
use serde::Deserialize;

pub use crate::model::Model;
use crate::options::BuildOptions;

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum Project {
    Bootloader,
    Boardloader,
    #[value(name = "bootloader_ci")]
    BootloaderCi,
    Firmware,
    Prodtest,
    Kernel,
    Secmon,
}

impl Project {
    /// The Rust package name used for cargo commands
    pub fn package_name(self, emulator: bool) -> &'static str {
        match self {
            Project::Bootloader => "bootloader",
            Project::Boardloader => "boardloader",
            Project::BootloaderCi => "bootloader_ci",
            Project::Firmware => {
                if emulator {
                    "unix"
                } else {
                    "firmware"
                }
            }
            Project::Prodtest => "prodtest",
            Project::Kernel => "kernel",
            Project::Secmon => "secmon",
        }
    }

    /// The linker binary type used by `build_and_link()`. This determines
    /// the `.map` filename generated by the linker.
    pub fn binary_name(self) -> &'static str {
        match self {
            Project::Bootloader => "bootloader",
            Project::BootloaderCi => "bootloader_ci",
            Project::Boardloader => "boardloader",
            Project::Firmware => "firmware",
            Project::Prodtest => "prodtest",
            Project::Kernel => "kernel",
            Project::Secmon => "secmon",
        }
    }

    /// The artifact name used for collected build outputs.
    /// Appends `-emu` for emulator builds.
    pub fn artifact_name(self, emulator: bool) -> String {
        let base = self.binary_name();
        if emulator {
            format!("{base}-emu")
        } else {
            base.to_string()
        }
    }

    /// Get symbol from memory.ld used as start address for flashing
    pub fn flash_start_symbol(self) -> Result<&'static str> {
        let symbol = match self {
            Project::Boardloader => "BOARDLOADER_START",
            Project::Bootloader | Project::BootloaderCi => "BOOTLOADER_START",
            Project::Firmware | Project::Prodtest => "FIRMWARE_START",
            _ => return Err(anyhow!("Flashing {:?} is not supported", self)),
        };
        Ok(symbol)
    }

    /// Returns the next project in the dependency chain,
    /// (e.g. Firmware -> Kernel -> Secmon)
    pub fn dependency(self, model: Model) -> Result<Option<Project>> {
        match self {
            Project::Firmware => Ok(Some(Project::Kernel)),
            Project::Kernel => {
                let has_secmon = model.config()?.secmon;
                Ok(if has_secmon {
                    Some(Project::Secmon)
                } else {
                    None
                })
            }
            _ => Ok(None),
        }
    }

    /// Returns whether the project can be flashed to a device
    /// using the `flash` subcommand.
    pub fn flashable(self) -> bool {
        self.flash_start_symbol().is_ok()
    }

    /// Returns whether the project can be uploaded to a device
    /// using the `upload` subcommand.
    pub fn uploadable(self) -> bool {
        matches!(self, Project::Firmware | Project::Prodtest)
    }
}

#[derive(ValueEnum, Debug, Clone, Copy, Deserialize, PartialEq, Eq)]
pub enum ConsoleType {
    Vcp,
    Swo,
    SystemView,
}

#[derive(Parser, Debug)]
#[command(name = "xtask")]
#[command(about = "Trezor workspace automation tasks")]
pub struct Cli {
    #[command(subcommand)]
    pub command: Cmd,
}

#[derive(Subcommand, Debug)]
pub enum Cmd {
    /// Build a project with the specified configuration
    Build(BuildArgs),
    /// Run clippy command with the specified configuration
    Clippy(BuildArgs),
    /// Run check command with the specified configuration
    Check(BuildArgs),
    /// Run tests of specified package
    Test(TestArgs),
    /// Clean build artifacts
    Clean,
    /// Format code with rustfmt
    Fmt,
    /// Flash firmware to device using OpenOCD
    Flash(FlashArgs),
    /// Erase flash or part of it using OpenOCD
    FlashErase(FlashEraseArgs),
    /// Reset the connected device using OpenOCD
    Reset(ResetArgs),
    /// Upload firmware to device
    Upload(UploadArgs),
    /// Combine multiple firmware projects into a single binary for flashing
    Combine(CombineArgs),
}

#[derive(Args, Debug, Clone)]
#[command(override_usage = "xtask build <PROJECT> --model <MODEL> [OPTIONS]")]
pub struct BuildArgs {
    pub project: Project,

    /// Build target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,

    /// Use emulator build
    #[arg(long, short = 'e')]
    pub emulator: bool,

    #[command(flatten)]
    pub options: BuildOptions,
}

#[derive(Args, Debug)]
pub struct TestArgs {
    /// Package to test
    pub packages: Vec<String>,
}

#[derive(Args, Debug)]
pub struct FlashArgs {
    pub project: Project,

    /// Target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,
}

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum FlashSection {
    All,
    Boardloader,
    Bootloader,
    Firmware,
    Storage,
}

#[derive(Args, Debug)]
pub struct FlashEraseArgs {
    /// Flash section to erase (default All)
    #[arg(default_value = "all")]
    pub section: FlashSection,

    /// Target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,
}

#[derive(Args, Debug)]
pub struct ResetArgs {
    /// Target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,
}

#[derive(Args, Debug)]
pub struct UploadArgs {
    pub project: Project,

    /// Build target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,
}

#[derive(Args, Debug)]
pub struct CombineArgs {
    pub project: Project,

    /// Target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,
}
