use anyhow::{Result, anyhow, bail};
use clap::{Args, Parser, Subcommand, ValueEnum};
use std::process;

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum Model {
    #[value(name = "d001")]
    D001,
    #[value(name = "d002")]
    D002,
    #[value(name = "t2t1")]
    T2T1,
    #[value(name = "t2b1")]
    T2B1,
    #[value(name = "t3b1")]
    T3B1,
    #[value(name = "t3t1")]
    T3T1,
    #[value(name = "t3w1")]
    T3W1,
}

impl Model {
    pub fn feature_name(self) -> &'static str {
        match self {
            Model::D001 => "model_d001",
            Model::D002 => "model_d002",
            Model::T2T1 => "model_t2t1",
            Model::T2B1 => "model_t2b1",
            Model::T3B1 => "model_t3b1",
            Model::T3T1 => "model_t3t1",
            Model::T3W1 => "model_t3w1",
        }
    }

    pub fn target_triple(self) -> &'static str {
        match self {
            Model::D001 | Model::T2T1 | Model::T2B1 => "thumbv7em-none-eabihf",
            Model::D002 | Model::T3B1 | Model::T3T1 | Model::T3W1 => "thumbv8m.main-none-eabihf",
        }
    }

    pub fn model_id(self) -> &'static str {
        match self {
            Model::D001 => "D001",
            Model::D002 => "D002",
            Model::T2T1 => "T2T1",
            Model::T2B1 => "T2B1",
            Model::T3B1 => "T3B1",
            Model::T3T1 => "T3T1",
            Model::T3W1 => "T3W1",
        }
    }

    pub fn has_optiga(self) -> bool {
        matches!(self, Model::T2B1 | Model::T3B1 | Model::T3T1 | Model::T3W1)
    }

    pub fn has_tropic(self) -> bool {
        matches!(self, Model::T3W1)
    }

    pub fn openocd_target(self) -> &'static str {
        match self {
            Model::D001 | Model::T2T1 | Model::T2B1 => "target/stm32f4x.cfg",
            Model::D002 | Model::T3B1 | Model::T3T1 | Model::T3W1 => "target/stm32u5x.cfg",
        }
    }
}

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum Component {
    Bootloader,
    Boardloader,
    #[value(name = "bootloader_ci")]
    BootloaderCi,
    Firmware,
    Prodtest,
    Kernel,
    Secmon,
}

impl Component {
    pub fn package_name(self, emulator: bool) -> &'static str {
        match self {
            Component::Bootloader => "bootloader",
            Component::Boardloader => "boardloader",
            Component::BootloaderCi => "bootloader_ci",
            Component::Firmware => emulator.then_some("unix").unwrap_or("firmware"),
            Component::Prodtest => "prodtest",
            Component::Kernel => "kernel",
            Component::Secmon => "secmon",
        }
    }

    /// The linker binary type used by `build_and_link()`. This determines
    /// the `.map` filename generated by the linker.
    pub fn binary_name(self) -> &'static str {
        match self {
            Component::Bootloader => "bootloader",
            Component::BootloaderCi => "bootloader_ci",
            Component::Boardloader => "boardloader",
            Component::Firmware => "firmware",
            Component::Prodtest => "prodtest",
            Component::Kernel => "kernel",
            Component::Secmon => "secmon",
        }
    }

    /// The artifact name used for collected build outputs.
    /// Appends `-emu` for emulator builds.
    pub fn artifact_name(self, emulator: bool) -> String {
        let base = self.binary_name();
        if emulator {
            format!("{base}-emu")
        } else {
            base.to_string()
        }
    }

    /// Get symbol from memory.ld used as start address for flashing
    pub fn flash_start_symbol(self) -> Result<&'static str> {
        let symbol = match self {
            Component::Boardloader => "BOARDLOADER_START",
            Component::Bootloader | Component::BootloaderCi => "BOOTLOADER_START",
            Component::Firmware | Component::Prodtest => "FIRMWARE_START",
            _ => return Err(anyhow!("Flashing {:?} is not supported", self)),
        };
        Ok(symbol)
    }

    pub fn flashable(self) -> bool {
        self.flash_start_symbol().is_ok()
    }

    pub fn uploadable(self) -> bool {
        matches!(self, Component::Firmware | Component::Prodtest)
    }

    pub fn frozen_default(self, emulator: bool) -> bool {
        match self {
            Component::Firmware => !emulator,
            _ => false,
        }
    }
}

#[derive(ValueEnum, Debug, Clone, Copy, PartialEq, Eq)]
pub enum ConsoleType {
    Vcp,
    Swo,
    SystemView,
}

#[derive(Parser, Debug)]
#[command(name = "xtask")]
#[command(about = "Trezor workspace automation tasks")]
pub struct Cli {
    #[command(subcommand)]
    pub command: Cmd,
}

#[derive(Subcommand, Debug)]
pub enum Cmd {
    /// Build a component with the specified configuration
    Build(BuildArgs),
    /// Run clippy command with the specified configuration
    Clippy(BuildArgs),
    /// Run check command with the specified configuration
    Check(BuildArgs),
    /// Run tests of specified package
    Test(TestArgs),
    /// Clean build artifacts
    Clean,
    /// Format code with rustfmt
    Fmt,
    /// Flash firmware to device using OpenOCD
    Flash(FlashArgs),
    /// Upload firmware to device
    Upload(UploadArgs),
}

#[derive(Args, Debug, Copy, Clone)]
#[command(override_usage = "xtask build <COMPONENT> --model <MODEL> [OPTIONS]")]
pub struct BuildArgs {
    pub component: Component,

    /// Build target model
    #[arg(long, short = 'm', ignore_case = true)]
    pub model: Model,

    /// Use emulator build
    #[arg(long, short = 'e')]
    pub emulator: bool,

    /// Enable debug build
    #[arg(long, short = 'd', num_args = 0..=1, default_missing_value = "true")]
    pub debug: Option<bool>,

    /// Debug console backend
    #[arg(long)]
    pub dbg_console: Option<ConsoleType>,

    /// Build Bitcoin-only firmware
    #[arg(long)]
    pub btc_only: bool,

    /// Enable production build
    #[arg(long)]
    pub production: bool,

    /// Use QA bootloader
    #[arg(long)]
    pub bootloader_qa: bool,

    /// Use dev bootloader
    #[arg(long)]
    pub bootloader_devel: bool,

    /// Enable unsafe firmware features
    #[arg(long)]
    pub unsafe_fw: bool,

    /// Embed frozen MicroPython modules
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub frozen: Option<bool>,

    /// Include MicroPython source lines
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub source_lines: Option<bool>,

    /// Optimize MicroPython bytecode
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub pyopt: Option<bool>,

    /// Enable Micropython memory performance measurements
    #[arg(long)]
    pub mem_perf: bool,

    /// Enable debug link
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub debug_link: Option<bool>,

    /// Disable UI animations
    #[arg(long)]
    pub disable_animation: bool,

    /// Show UI perf overlay
    #[arg(long)]
    pub perf_overlay: bool,

    /// Include crypto benchmarks
    #[arg(long)]
    pub benchmark: bool,

    /// Log stack usage
    #[arg(long)]
    pub log_stack_usage: bool,

    /// Use blocking VCP writes
    #[arg(long)]
    pub block_on_vcp: bool,

    /// Enable Address Sanitizer (ASAN) instrumentation
    #[arg(long)]
    pub asan: bool,

    /// Enable external app loading
    #[arg(long)]
    pub apps: bool,

    /// Disable OPTIGA support
    #[arg(long)]
    pub disable_optiga: bool,

    /// Disable TROPIC support
    #[arg(long, num_args = 0..=1, default_missing_value = "true")]
    pub disable_tropic: Option<bool>,

    /// Enable insecure storage test mode
    #[arg(long)]
    pub storage_insecure_testing_mode: bool,

    /// Output cargo timings
    #[arg(long)]
    pub timings: bool,

    /// Enable verbose output
    #[arg(long)]
    pub verbose: bool,
}

impl BuildArgs {
    pub fn resolve_features(&self) -> Result<Vec<&'static str>> {
        let mut features = vec![self.model.feature_name()];

        if self.emulator {
            features.push("emulator");
        }

        if self.production {
            features.push("production");
        }

        if self.bootloader_devel {
            features.push("bootloader_devel");
        }

        if self.bootloader_qa {
            features.push("bootloader_qa");
        }

        if self.emulator {
            features.push("dbg_console");
        } else {
            match (self.component, self.dbg_console) {
                (Component::Firmware, Some(_)) => features.push("dbg_console"),
                (Component::Secmon, Some(ConsoleType::Vcp)) => (),
                (Component::Boardloader, Some(ConsoleType::Vcp)) => (),
                (Component::Prodtest, Some(ConsoleType::Vcp)) => (),
                (_, Some(ConsoleType::Vcp)) => features.push("dbg_console_vcp"),
                (_, Some(ConsoleType::Swo)) => features.push("dbg_console_swo"),
                (_, Some(ConsoleType::SystemView)) => features.push("dbg_console_sysview"),
                (_, None) => (),
            }
        }

        let pyopt = self.pyopt.unwrap_or(!self.emulator);

        if self.component == Component::Firmware {
            if pyopt {
                features.push("pyopt");
            } else {
                features.push("debug");
            }

            if self.source_lines.unwrap_or(self.emulator) {
                features.push("micropy_enable_source_line");
            }

            if self.benchmark {
                features.push("benchmark");
            }

            if self.log_stack_usage {
                features.push("log_stack_usage");
            }

            if self.block_on_vcp {
                features.push("block_on_vcp");
            }

            if self.apps {
                features.push("app_loading");
            }

            if self.mem_perf {
                features.push("memperf");
            }

            if !self.production {
                features.push("dev_keys");
            }
        }

        if matches!(
            self.component,
            Component::Secmon | Component::Kernel | Component::Firmware
        ) {
            if !self.btc_only {
                features.push("universal_fw");
            }

            if !pyopt {
                features.push("optiga_testing");
            }

            if self.unsafe_fw {
                features.push("unsafe_fw");
            }

            if self.storage_insecure_testing_mode {
                if self.production {
                    bail!("storage_insecure_testing_mode cannot be enabled in production builds");
                }
                features.push("storage_insecure_testing_mode");
            }
        }

        if matches!(
            self.component,
            Component::Firmware | Component::Bootloader | Component::Prodtest
        ) {
            if self.perf_overlay {
                features.push("ui_performance_overlay");
            }

            if self.debug_link.unwrap_or(self.emulator) {
                features.push("debuglink");
                features.push("ui_debug");
            }

            if self.disable_animation {
                features.push("disable_animation");
            }
        }

        if matches!(
            self.component,
            Component::Firmware | Component::Kernel | Component::Secmon | Component::Prodtest
        ) {
            if self.model.has_optiga() && !self.disable_optiga {
                features.push("optiga");
            }

            if self.model.has_tropic() && !self.disable_tropic.unwrap_or(self.emulator) {
                features.push("tropic");
            }
        }

        // By default, we want to build with `frozen` for hardware targets
        if self
            .frozen
            .unwrap_or(self.component.frozen_default(self.emulator))
        {
            features.push("frozen");
        }

        Ok(features)
    }

    // Configures the cargo command with the appropriate arguments and features
    // based on the provided cli arguments
    pub fn configure_cargo(&self, cmd: &mut process::Command) -> Result<()> {
        let features = self.resolve_features()?;

        cmd.args(["--package", self.component.package_name(self.emulator)]);
        cmd.args(["--features", &features.join(",")]);

        if !self.emulator {
            cmd.arg("-Zbuild-std=core");
            cmd.arg("-Zbuild-std-features=panic_immediate_abort");
            cmd.args(["--target", self.model.target_triple()]);
        }

        // 1. see https://nnethercote.github.io/perf-book/type-sizes.html#measuring-type-sizes for more details
        // 2. Adds an ELF section with Rust functions' stack sizes. See the following links for more details:
        // - https://doc.rust-lang.org/nightly/unstable-book/compiler-flags/emit-stack-sizes.html
        // - https://blog.japaric.io/stack-analysis/
        // - https://github.com/japaric/stack-sizes/
        // !@# cmd.env("RUSTFLAGS", "-Z print-type-sizes -Z emit-stack-sizes");

        if self.emulator && self.asan {
            // # workaround for sanitizers being nightly-only
            //  remove after stabilized https://github.com/rust-lang/rust/issues/39699
            // !@# TODO 'RUSTC_BOOTSTRAP': '1',
            cmd.args(["-Z", "sanitizer=address"]);
        }

        // We have different default for `--debug` for hardware and emulator builds
        if !self.debug.unwrap_or(self.emulator) {
            cmd.arg("--release");
        }

        if self.timings {
            cmd.arg("--timings");
        }

        if self.verbose {
            cmd.arg("--verbose");
        }

        Ok(())
    }
}

