//   Framework CLI  :     1 –     9  (defined in cli.h, not here)
//   backup-ram     :  1000 –  1999
//   ble            :  2000 –  2999
//   bootloader     :  3000 –  3999
//   button         :  4000 –  4999
//   haptic         :  5000 –  5999
//   manufacturing  :  6000 –  6999
//   nfc            :  7000 –  7999
//   nrf            :  8000 –  8999
