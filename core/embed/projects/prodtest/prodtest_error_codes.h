//   otp            : 10000 – 10999  (otp-batch + otp-device-sn share a helper)
//   otp-variant    : 11000 – 11999
//   power-manager  : 12000 – 12999
//   rtc            : 13000 – 13999
//   sdcard         : 14000 – 14999
//   secrets        : 15000 – 15999
//   secure-channel : 16000 – 16999
//   tamper         : 17000 – 17999
//   telemetry      : 18000 – 18999
//   touch          : 19000 – 19999
//   tropic         : 20000 – 20999
