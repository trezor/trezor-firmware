// DISPLAY      : DISC2 && MCU
// BUSY (OUT)   : PB11 (CN9 pin 24, IN/SPI2_RDY)
// RES  (IN)    : PD0 (CN10 pin 35, OUT)
// D/C  (IN)    : PD9 (CN10 pin 11, OUT)
// CS   (IN)    : PB12 (CN10 pin 17, SPI2 NSS)
// SCK  (IN)    : PB13 (CN10 pin 15, SPI2 SCK)
// SDI  (IN/OUT): PD4 (CN10 pin 13, SPI2 MOSI)
// GND          : GND (CN10 pin 8)
// 3.3V         : 3.3V (CN9 pin 7)

// BUSY: Device Busy Signal. This pin is Busy state output pin. When Busy is
// High, the operation of the chip should not be interrupted, and command should
// not be sent. For example., The chip would output Busy pin as High when
// - Outputting display waveform; or
// - Programming with OTP
// - Communicating with digital temperature sensor
// RES: System Reset. This pin is reset signal input. Active Low.
// D/C: Logic Control. This pin is Data/Command control pin connecting to the
// MCU. Refer to MCU interface in Section 6.1.
// CS： Logic Control. This pin is the chip select input connecting to the MCU.
// Refer to MCU interface in Section 6.1.
// SCL: Data Bus. This pin is serial clock pin for interface. Refer to MCU
// interface in Section 6.1.
// SDA： Data Bus. This pin is serial data pin for interface. Refer to MCU
// interface in Section 6.1.
